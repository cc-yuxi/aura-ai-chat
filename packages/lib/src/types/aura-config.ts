  mcpServers?: McpServerConfig[];
  /** Implementation for conversation storage. */
  conversationManager?: IConversationManager;
  /** Pre-defined conversation ID to load. */
  conversationId?: string;
  /** Maximum number of tokens for context window. */
  maxContextTokens?: number;
  /** Whether to enable response streaming. */
  enableStreaming?: boolean;
  /** Extra safety constraints for the AI. */
  additionalSafetyInstructions?: string;
  /** Max iterations the agent can take per message. */
  maxIterations?: number;
  /** Whether to show the AI's step-by-step reasoning. */
  showThinkingProcess?: boolean;
  /** Default timeout for tool executions. */
  toolTimeout?: number;
  /** Timeout for pending user confirmations. */
  confirmationTimeoutMs?: number;
  /** Whether to enable WebMCP client for browser-side MCP servers. */
  enableWebMcp?: boolean;
