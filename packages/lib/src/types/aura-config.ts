/**
 * Interface for implementing custom storage for conversations and tool logs.
 */
export interface IConversationManager {
  /** Create a new conversation record. */
  createConversation?(conversation: Conversation): Promise<Conversation>;
  /** Get a single conversation by ID. */
  getConversation?(id: string): Promise<Conversation | undefined>;
  /** Load a full conversation into the chat. */
  loadConversation?(conversationId: string): Promise<Conversation | null>;
  /** List all available conversations for the user. */
  listConversations?(): Promise<Conversation[]>;
  /** Save a message to the persistent store. */
  saveMessage(
    conversationId: string,
    message: ChatMessage,
  ): Promise<void> | Promise<unknown>;
  /** Delete a conversation. */
  deleteConversation?(conversationId: string): Promise<void>;
  /** Clear all conversation history. */
  clearHistory?(): Promise<void>;
}
